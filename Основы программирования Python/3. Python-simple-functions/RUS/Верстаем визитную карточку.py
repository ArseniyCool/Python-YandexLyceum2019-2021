# Чтобы сделать карточку с именем тому или иному человеку нужен шаблон, который
# подходил бы к любому клиенту - для этого -
# Данная программа выводит количество пропусков, которое нужно оставить для имени человека
the_longest_name = len(input('Введите самое длинное имя,которое Вы знаете:'))
the_longest_name = the_longest_name * 2 + 3
print('Поставьте в Вашей визитной карточке', the_longest_name, 'пропусков.')
